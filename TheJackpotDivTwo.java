	public int[] find(int[] money, int jackpot)
	{
		int x = jackpot;
		int i=0;
		while (x > 0) {
			int minn = 0;
			for (i = 1 ; i < money.length ; i++){
				if (money[i] < money[minn]) {
					minn = i;
				}
			}
			money[minn] = money[minn] + 1;
			x--;
		}
		Arrays.sort(money);
		return money;
	}

