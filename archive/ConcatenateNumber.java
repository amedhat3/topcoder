	public int getSmallest(int number, int k)
	{
		BigInteger k2 = BigInteger.valueOf(k);
		BigInteger n = BigInteger.valueOf(number);

//		long n = number;
		int i=0;
		while(i<100000){
//			System.out.println(n.mod(k2));
			if(n.mod(k2) == BigInteger.ZERO){
				i++;
				return i;
			}
			else{
				try{
				String s = n.toString();
				s += Long.toString(number);
				n = new BigInteger(s);
				i++;
				}
				catch(Exception e)
				{
					break;
				}
			}
		}
		return -1;
	}
	
