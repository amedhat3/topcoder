	public int bestSchedule(int N, int K, int[] w)
	{

		int minn = 999999;
		for(int i = 0 ; i+K-1 < N ;i++){
			int yy =0;
			for(int j =0 ; j < K; j++){
				for(int v = 0 ; v < w.length ; v++){
					if(i+j+1 == w[v]){
						yy++;
						break;
					}
				}
			}
			minn = Math.min(yy,minn);
		}

		return minn;
	}

