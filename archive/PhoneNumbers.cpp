class PhoneNumbers {
public:
	string bestNumber(string);
};

string PhoneNumbers::bestNumber(string n) {

	int us[n.sz];
	mem (us,0);
	int q =0;
	for(int i =0 ; i < n.sz;i++){
		if(n[i]==n[i+1]){
			i++;
			us[i]=1;
			us[i+1] =1;
			q+=2;
		}
	}

	for(int i =0 ; i < n.sz-1;i++){
		if( us[i] || us[i+1] || us[i+2] != 1){
			if(n[i]==n[i+2]){
				i+=3;
				us[i]=1;
				us[i+1] =1;
				us[i+2]=1;
				q+=1;
			}
		}
	}

	cout <<q<<endl;
	return "";
}


