class RobotSimulation {
public:
	int cellsVisited(string, int);
};

int RobotSimulation::cellsVisited(string program, int times) {

	char *s;
	long siz;
	siz = times*program.sz;
	s = new char[siz];
	for(long i =0;i<times;i++)
	{
		for(int j = 0 ; j<program.sz; j ++)
		s[i*program.sz+j]=program[j];
	}
	long count=1;
	long x,y;
	vpii vis;
	x=0;
	y=0;
	pii tmp;
	long i=0;
	long j=0;
	tmp = mp(x,y);
	vis.pb(tmp);
	for(i=0;i<siz;i++){
		if(s[i]=='U')x++;
		else if(s[i]=='D')x--;
		else if(s[i]=='L')y--;
		else if(s[i]=='R')y++;
		tmp = mp(x,y);
		for(j =0 ; j <vis.sz ; j++){
			if(tmp.first == vis[j].first && tmp.second == vis[j].second){
				break;
			}
		}
		if(j >= vis.sz){
			count++;
			vis.pb(tmp);
		}
	}
	
	return count;
	
	
}


